#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*temp;

	temp = lst;
	new = NULL;
	while (lst)
	{
		temp = ft_lstnew(f(lst ->content));
		if (!temp)
			ft_lstdelone(temp, del);
		ft_lstadd_back(&new, temp);
		lst = lst ->next;
	}
	return (new);
}

